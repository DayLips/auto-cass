    def __init__(self, db: Session):
        self.category_repository = CategoryRepository(db)
        self.product_repository = ProductRepository(db)
        self.shop_repository = ShopRepository(db)
        self.cass_repository = CassRepository(db)

    def check_category(self) -> bool:
        count_category = self.category_repository.get_count()
        return True if count_category > 0 else False
    
    def check_product(self) -> bool:
        count_product = self.product_repository.get_count()
        return True if count_product > 0 else False
    
    def check_shop(self) -> bool:
        count_shop = self.shop_repository.get_count()
        return True if count_shop > 0 else False
    
    def check_cass(self) -> bool:
        count_cass = self.cass_repository.get_count()
        return True if count_cass > 0 else False
    
    def create_data_in_category(self) -> bool:
        categories = list(self.CATEGORY_ITEMS.keys())
        for category in categories:
            category_data = CategoryCreate(
                name=category,
                description=self.CATEGORY_DESC[category]
            )
            category_db = self.category_repository.create(category_data)
            if not category_db:
                return False
            
        return True
    
    def create_data_in_product(self) -> bool:
        categories = list(self.CATEGORY_ITEMS.keys())
        for category in categories:
            category_db = self.category_repository.get_by_name(category)
            if category_db is None:
                return False
            else:
                for item in self.CATEGORY_ITEMS[category]:
                    item_data = ProductCreate(
                        category_id=category_db.id,
                        description=self.ITEM_DESC[item],
                        name=item
                    )

                    item_db = self.product_repository.create(item_data)
                    if not item_db:
                        return False
        
        return True
    
    def create_data_in_shop(self) -> bool:
        num = 1
        for shop in self.SHOPS.keys():
            shop_data = ShopCreate(
                description=self.SHOPS[shop],
                name=shop,
                num_shop=num,
            )

            shop_db = self.shop_repository.create(shop_data)
            if not shop_db:
                return False
            else:
                num += 1

        return True

    def create_data_in_cass(self) -> bool:
        casses = list(range(1, 5))
        shops = self.shop_repository.get_all()
        for shop in shops:
            shop_id = shop.id
            
            for i in casses:
                total_money = random.uniform(10000.0, 100000.0)
                cass_data = CassCreate(
                    shop_id=shop_id,
                    num_cass=i,
                    available=True,
                    total_money=Decimal(str(total_money))
                )

                cass_db = self.cass_repository.create(cass_data)

                if not cass_db:
                    return False
        
        return True